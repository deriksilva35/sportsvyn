function esc(s) {
  return String(s ?? '').replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;');
}

// sections[] -> clean semantic HTML: <h2> per heading, <p> per paragraph. No
// inline styles, no legacy classes - the reader typography styles it.
export function sectionsToHtml(sections) {
  return (Array.isArray(sections) ? sections : []).map((s) => {
    const h = s.heading ? `<h2>${esc(s.heading)}</h2>` : '';
    const paras = String(s.body ?? '')
      .split(/\n\n+/)
      .map((p) => p.trim())
      .filter(Boolean)
      .map((p) => `<p>${esc(p)}</p>`)
      .join('\n');
    return [h, paras].filter(Boolean).join('\n');
  }).join('\n\n');
}

export function slugify(s) {
  return String(s ?? '')
    .toLowerCase().normalize('NFD').replace(/[̀-ͯ]/g, '')
    .replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '').slice(0, 80) || 'article';
}

// Reserve a unique slug against existing articles (base, base-2, base-3, ...).
export async function uniqueArticleSlug(base) {
  const root = slugify(base);
  let slug = root;
  for (let n = 2; n < 100; n++) {
    const hit = await sql`SELECT 1 FROM articles WHERE slug = ${slug} LIMIT 1`;
    if (hit.length === 0) return slug;
    slug = `${root}-${n}`;
  }
  return `${root}-${Date.now()}`;
}
